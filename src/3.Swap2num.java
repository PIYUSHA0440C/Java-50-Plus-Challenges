class Swap2num {
    public static void main(String[] args) {
        int a = 10, b = 15, temp;
        System.out.println("The value of a is "+a+" and the value of b is "+b+".");
        temp = a;
        a = b;
        b = temp;
        System.out.println("The value of a is "+a+" and the value of b is "+b+".");
    }
}
